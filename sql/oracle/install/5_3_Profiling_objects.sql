)
AND atc.data_type LIKE '%CHAR%'/ --HARDCODE FOR DEPLOYMENT

CREATE OR REPLACE VIEW PROFILE_VIEW_SORTED_BY_ROWS_COUNT AS 
SELECT OWNER, TABLE_NAME, COLUMN_NAME, NUM_ROWS
FROM   profile_table_with_nums
ORDER  BY num_rows DESC/ --HARDCODE FOR DEPLOYMENT


CREATE OR REPLACE VIEW PROFILE_VIEW_BY_ROWS_ASC AS 
SELECT owner, table_name, column_name, ROW_NUMBER() OVER (ORDER BY num_rows ASC) AS idx
FROM profile_view_sorted_by_rows_count/ --HARDCODE FOR DEPLOYMENT


