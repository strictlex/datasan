CREATE OR REPLACE VIEW PROFILE_VIEW_WITH_NUMS AS
SELECT atc.owner, atc.table_name, atc.column_name, at.num_rows
FROM all_tab_columns atc
JOIN all_tables at ON atc.owner = at.owner AND atc.table_name = at.table_name
WHERE atc.owner NOT IN (
    'SYS', 'SYSTEM', 'OUTLN', 'AUDSYS', 'DBSNMP', 'APPQOSSYS', 
    'GSMADMIN_INTERNAL', 'DBSFWUSER', 'XDB', 'WMSYS', 'OJVMSYS', 
    'CTXSYS', 'ORDSYS', 'ORDDATA', 'MDSYS', 'OLAPSYS', 'LBACSYS', 
